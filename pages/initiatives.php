      <td>
        <p>Often, the complainant does not know the action taken on his complaint. There is no existing provision to keep the victims informed whether some arrests have been made in a particular case and the progress of the investigation.<br />
    <br />
          
          
          In order to make the police services more transparent and accountable to general public, scheme of "GRIEVANCE REDRESSAL DAY" at every police station in between 09:00 A.M. to 11:30 A.M. has been launched.   <br />
  <br />
          
          
          During these "GRIEVANCE REDRESSAL DAY" Sr. P.I. of the Police Station, P.I. Crime and 02 Investigation officers remain present with the status of the cases. The concerned complainants are invited. They are briefed about the progress of the investigation of their case.<br />
  <br />
          
          
          Zonal D.C.P's & Divisional A.C.P.'s will present one of the police station of their juridication for Grievance Redressal Day.<br />
  <br />
          
          
          This scheme has received enthusiastic response from the members of public.
